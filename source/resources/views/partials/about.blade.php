    <h3>About the festival</h3>
    <h4 class="blue">
        Proudly Partnered With
        <strong><a href="http://www.flyinfestival.com" target="_blank">Fly In Festival</a></strong>
    </h4>
    <br>
    <p>
        The <a href="http://www.flyinfestival.com" target="_blank">Fly In Festival</a>
        is a Bluegrass, Old Time Music, Americana, and Acoustic Music Festival
        held the last weekend in August. The festival is located on the
        beautiful Robert Newlon Airpark & Campground, along the Ohio River just
        North of Huntington (Lesage) West Virginia. The festival grounds will
        opens the evening before the event for camping, jamming, and gathering.
        Some of the region's best Old Time, Bluegrass and Americana Bands will
        be performing, as well as Air Plane Tours, Sky Diving, and Camping.
        Tickets are $30/Day, $50/Weekend. Primitive Camping is $20/weekend, and
        a limited number of Full Hook-Up RV site are also available. Food
        and Drinks will be sold by the Fly In Cafe. Call (304) 733-1240 for
        more information.
    </p>
